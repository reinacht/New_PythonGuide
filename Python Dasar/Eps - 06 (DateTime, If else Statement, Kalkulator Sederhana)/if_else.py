# Flow Code (If, Else, Elif statement)
# If
# Konditionen
# Aktionen


# 1. If
name = input("Masukkan Nama : ")
print(name)
if name == "Barok":
    print(f"Hai {name}, Anda berarti yang Utama!")
# 2. Elif
elif name == "DorberkW":
    print(f"Hai {name}, Anda yang paling pintar di sini! Halo sekali lagi Jenius!")
elif name == "Rei":
    print(f"Hai {name}, Anda lagi naik daun, kan?")
# 3. Else
else:
    print(f"Maaf {name}, Mungkin Anda yang ke-terakhir atau Kami yang tidak mengenali Anda")
print("Selamat Malam, Sayonara!")
