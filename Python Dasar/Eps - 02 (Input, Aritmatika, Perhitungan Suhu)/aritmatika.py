# aritmatika dalam pyhton ada 7
# +   = penjumlahan
a = 3
b = 7

hasil = a + b
print(a, '+', b, '=', hasil)

# -   = pengurangan
hasil = a - b
print(a, '-', b, '=', hasil)

# *   = perkalian
hasil = a * b
print(a, '*', b, '=', hasil)

# /   = pembagian
hasil = a / b
print(a, '/', b, '=', hasil)

# **  = perpangkatan
hasil = a ** b
print(a, '**', b, '=', hasil)

# %   = modulus, hasil sisa bagi (10 / 3 = 3 dan bersisa 1) = (10 % 3 = 1)
hasil = a % b
print(a, '%', b, '=', hasil)

# //  = floor division, hasil bagi yang dibulatkan ke bawah
hasil = a // b
print(a, '//', b, '=', hasil)


# priority
# 1. ()
# 2. eksponen = **
# 3. perkalian dan sebagainya = * / % //
# 4. penjumlahan dan pengurangan = + -

x = 3
y = 9
z = 2

hasil = y ** x + z % 1 - x // z
print('Hasil dari', y, '**', x, '+', z, '% 1 -', x, '//', z, ' : ', hasil)
