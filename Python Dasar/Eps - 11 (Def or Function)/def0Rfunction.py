# pengenalan fungsi

def mencetak():
    print("Ini fungsi sederhana")

mencetak()


# fungsi dengan argumen
def cetak(angka):
    hasil = angka - 999
    print(hasil)

cetak(7)


# fungsi dengan return
def hitung(angka1, angka2, angka3):
    hasil = angka1 * angka2 * angka3
    return hasil

result = hitung(3, 8, 9)
print(result)


# fungsi dengan default argument
def count(angka1, angka2, angka3=69):
    hasil = angka1 * angka2 * angka3
    return hasil

result = count(3, 8)
print(result)

def count(angka1=55, angka2=64, angka3=69):
    hasil = angka1 * angka2 * angka3
    return hasil

result = count()
print(result)
result = count(angka2=44)
print(result)
